package Model.Cards;

import Model.Players.Player;

/**
 * Each card contains an abstract action, and the action can be performed on a 
 * player.
 * @author Chris Berry
 */
public abstract class AbstractCardAction {
    
    /**
     * The appropriate action for a card is performed.
     * @param player to perform action on.
     * @param card which the action is being performed from.
     */
    public abstract void performAction(Player player, Card card);
}
